package models;

public class Staff {
  /*
   * Attributes
   */
  private String companyID;
  private String name;

  /*
   * Getters + Setters
   */
  // companyID
  public String getCompanyID() {
    return this.companyID;
  }
  public void setCompanyID(String companyID) {
    this.companyID = companyID;
  }

  // name
  public String getName() {
    return this.name;
  }
  public void setName(String name) {
    this.name = name;
  }
}
